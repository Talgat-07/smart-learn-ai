const withMeta = (directionId, topic, index) => ({
  ...topic,
  directionId,
  order: index + 1,
  theory:
    topic.theory ||
    `${directionTheoryIntro[directionId]} В теме «${topic.title}» вы изучите ключевые принципы и закрепите их на практике.`,
})

export const learningPlans = Object.fromEntries(
  Object.entries(rawLearningPlans).map(([directionId, topics]) => [
    directionId,
    topics.map((topic, index) => withMeta(directionId, topic, index)),
  ]),
)

export const allTopics = Object.values(learningPlans).flat()

export const topicsById = Object.fromEntries(
  allTopics.map((topic) => [topic.id, topic]),
)
